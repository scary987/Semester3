#include <stdio.h>


int main() { /* Eingaben und Ausgaben */
	
	int a,b,c;
	int max=0, sum=0;
	printf("Eingabe der Zahl a\n");
	scanf("%d",&a);
	fflush ( stdin );
	printf("Eingabe der Zahl b\n");
	scanf("%d",&b);
	fflush ( stdin );
	printf("Eingabe der Zahl c\n");
	scanf("%d",&c);
	fflush ( stdin );
	
	sum= a+b+c;
	if(a>b) max=a; else max=b;
 	if(c>max) max=c;
 
	
      	printf("Die Summe von %d, %d, %d ist %d, das Maximum ist %d\n", a,b, c, sum, max);
	

}

