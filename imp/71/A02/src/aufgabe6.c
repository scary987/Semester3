int main(){
    double i,n ,  x, sum, v;    
	printf("Schreiben Sie der Wert vom Winkel: ");
	scanf ("%lf",&x);
    x=x*3.14159265358/180;
    v=x;
    sum=x;
    n = 10 ;
	
    for(i=1;i<=n;i++)
    {
        v=(v*(-1)*x*x)/(2*i*(2*i+1));
        sum=sum+v;
    }
     
    printf(" Sin(%f) = %.9lf",x,sum);
	
	return 0; 
 
}
	
   